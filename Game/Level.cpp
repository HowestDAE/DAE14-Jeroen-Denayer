void Level::Draw(const Game::GameInfo& gameInfo) const
{
	const float TILE_SIZE{ float(gameInfo.TILE_SIZE_PIX_SCALED) };
	for (int r{ }; r < m_Data.size(); ++r)
	{
		for (int c{}; c < m_Data[r].size(); ++c)
		{
			int value{ m_Data[r][c] };
			if (value == 1)
				utils::SetColor(Color4f{ 0.5f, 0.5f, 0.5f, 1.f });
			else if (value == 0)
				utils::SetColor(Color4f{ 0.f, 0.f, 0.f, 1.f });

			float x{ c * TILE_SIZE };
			float y{ r * TILE_SIZE };
			utils::FillRect(x, y, TILE_SIZE, TILE_SIZE);
		}
	}

	//Draw debug grid lines
	utils::SetColor(Color4f{ 0.25f, 0.25f, 0.25f, 1.f });
	for (int row{ }; row < m_Rows; ++row)
	{
		float y{ row * TILE_SIZE };
		utils::DrawLine(Point2f{ 0.0f, y }, Point2f{ gameInfo.SCREEN_WIDTH, y });
	}
	for (int col{}; col < m_Cols; ++col)
	{
		float x{ col * TILE_SIZE };
		utils::DrawLine(Point2f{ x, 0.0f }, Point2f{ x, gameInfo.SCREEN_HEIGHT });
	}
}

TileIdx Level::GetTileIdxByPos(const Point2f& pos, const Game::GameInfo& gameInfo) const
{
	int colIdx{ int((pos.x - 1) / gameInfo.TILE_SIZE_PIX_SCALED) };
	int rowIdx{ int((pos.y - 1) / gameInfo.TILE_SIZE_PIX_SCALED) };
	return TileIdx{ rowIdx, colIdx };
}

int Level::GetTileID(int row, int col) const
{	
	if (row >= 0 && row < m_Rows && col >= 0 && col < m_Cols) return m_Data[row][col];
	else return -1;
}

Rectf Level::GetTileRect(TileIdx tileIdx, const Game::GameInfo& gameInfo) const
{
	int x{ tileIdx.c * gameInfo.TILE_SIZE_PIX_SCALED };
	int y{ tileIdx.r * gameInfo.TILE_SIZE_PIX_SCALED };
	return Rectf{ float(x), float(y), float(gameInfo.TILE_SIZE_PIX_SCALED), float(gameInfo.TILE_SIZE_PIX_SCALED) };
}

TileCorners Level::GetTileCorners(TileIdx tileIdx, const Game::GameInfo& gameInfo) const
{
	Rectf tileRect{ GetTileRect(tileIdx, gameInfo) };
	return TileCorners{
		Point2f{tileRect.left, tileRect.bottom + tileRect.height},
		Point2f{tileRect.left + tileRect.width, tileRect.bottom + tileRect.height},
		Point2f{tileRect.left, tileRect.bottom},
		Point2f{tileRect.left + tileRect.width, tileRect.bottom}
	};
}

void Level::FlipLevel()
{
	std::vector< std::vector<int> > dataCopy{ m_Data };
	for (int row{}; row < m_Data.size(); ++row)
	{
		m_Data[row] = dataCopy[m_Data.size() - 1 - row];
	}
}
